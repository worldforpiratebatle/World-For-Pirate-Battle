function changeLanguage(lang) {
    const texts = translations[lang];

    // Atualiza textos simples
    for (const key in texts) {
        const element = document.getElementById('txt-' + key);
        if (element) {
            element.innerText = texts[key];
        }
    }

    // Atualiza Placeholders (campos de input)
    const inpUser = document.getElementById('inp-user');
    if(inpUser) inpUser.placeholder = texts['placeholder-user'];
    
    const inpPass = document.getElementById('inp-pass');
    if(inpPass) inpPass.placeholder = texts['placeholder-pass'];
    
    const inpPirate = document.getElementById('inp-pirate');
    if(inpPirate) inpPirate.placeholder = texts['placeholder-pirate'];
    
    const inpSecret = document.getElementById('inp-secret');
    if(inpSecret) inpSecret.placeholder = texts['placeholder-secret'];
    
    const inpEmail = document.getElementById('inp-email');
    if(inpEmail) inpEmail.placeholder = texts['placeholder-email'];
}